public class Barrier {

    String type;
    String symbol;
    int lifePoints;
    int posZeile;
    int posSpalte;

    public Barrier(String type, String symbol, int lifePoints, int posZeile, int posSpalte) {
        this.type = type;
        this.symbol = symbol;
        this.lifePoints = lifePoints;
        this.posZeile = posZeile;
        this.posSpalte = posSpalte;
    }

}


